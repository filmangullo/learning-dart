void main () {
  var i = 0;

  i++; // i = i + 1;

  var j = 0;
  ++j; // i = i + 1;

  print(i);
  print(j);

  /** Post Increment
   *    for(i=0; i<5; i++)
   *    printf("%d ",i);
   *
   *    value = 0 1 2 3 4.
   *
   *  Pre Increment
   *    for(i = 0; i<5;)
   *    printf("%d ",++i);
   *
   *    value = 1 2 3 4 5;
   */
}